#Problemin tüm olasılıklarını sistematik olarak deneme
def is_valid(board, row, col, num):
    # Satır kontrolü
    if num in board[row]:
        return False

    # Sütun kontrolü
    for r in range(9):
        if board[r][col] == num:
            return False

    # 3x3 kutu kontrolü
    start_row, start_col = 3 * (row // 3), 3 * (col // 3)
    for r in range(start_row, start_row + 3):
        for c in range(start_col, start_col + 3):
            if board[r][c] == num:
                return False

    return True


def solve_sudoku(board):
    for row in range(9):
        for col in range(9):
            if board[row][col] == 0:  # boş hücre
                for num in range(1, 10):
                    if is_valid(board, row, col, num):
                        board[row][col] = num
                        if solve_sudoku(board):
                            return True
                        board[row][col] = 0  # geri dön
                return False
    return True


